public void showDataInTable()
    {
        try
        {
        String []ts=TimeSlotDao.getTs();
          
               Object[]rows=new Object[7];
               
                DefaultTableModel model=(DefaultTableModel)tblViewTT.getModel();
                 for(int slot=1;slot<=ts.length;slot++)
                 {
                     rows[0]=ts[slot-1];
                     for(int day=1;day<=6;day++){
                          if(semwise){      
                                tt=ViewTTDao.getSemTTData(day,slot,sem,batch);
                          }
                          else if(roomwise){    
                                tt=ViewTTDao.getRoomTTData(day,slot,roomname);
                          }
                          else if(facwise){      
                                tt=ViewTTDao.getFacTTData(day,slot,fname);
                          }                          
                          
                          if(tt != null)
                                rows[day]=tt.getSubName()+" "+tt.getFname()+" "+tt.getRname()+tt.getBatch();
                      
                     else 
                             rows[day]="";
                               
                     }
                         
                     model.addRow(rows);
                 }
        }
         catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in SQL!!","Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }


