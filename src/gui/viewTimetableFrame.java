    private void printbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_printbtnMouseClicked
        // TODO add your handling code here:
			File file = new File(this.getTitle().split(" ")[1]);
			String fi = file.getAbsolutePath() + ".csv";
			System.out.println(file);
			FileWriter out = null;
			try {
				out = new FileWriter(fi);
				for (int i = 0; i < tblViewTT.getColumnCount(); i++) {
					out.write(tblViewTT.getColumnName(i) + ",");
				}
				out.write("\n");
				for (int i = 0; i < tblViewTT.getRowCount(); i++) {
					for (int j = 0; j < tblViewTT.getColumnCount(); j++) {
						out.write(delHTMLTag(tblViewTT.getValueAt(i, j).toString()) + ",");
					}
					out.write("\n");
				}
			} catch (IOException e1) {
				e1.printStackTrace();
			} finally {
				try {
					out.close();
				} catch (IOException e1) {
					e1.printStackTrace();
				}
			}
			JOptionPane.showMessageDialog(null, "success");
    }//GEN-LAST:event_printbtnMouseClicked

