public void showDataInTable()
    {
        try
        {
           sa=SemesterDao.getAllSemester();
          
          if(sa.length==0)
                JOptionPane.showMessageDialog(null,"Sorry! No Sem Present!","Error!",JOptionPane.INFORMATION_MESSAGE);
            
            else
            {
               Object[]rows=new Object[4];
                
                DefaultTableModel model=(DefaultTableModel)tblSemester.getModel();
                 for(int i=0;i<sa.length;i++)
                 {
                     rows[0]=sa[i].getSemid();
                     rows[1]=sa[i].getSem();
                     rows[2]=sa[i].getBatch();
                     rows[3]=sa[i].getCap();
                     model.addRow(rows);
                 }
            }
        }
         catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in SQL!!","Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }



    private boolean validateInput()
    {
        String semInfo =txtSemNo.getText();
        String capacity=txtCapacity.getText();
        
        if(semInfo.isEmpty()||capacity.isEmpty())
        {
            return false;
        }
        else
        {
            batch=jcbBatch.getSelectedItem().toString();
            semNo=Integer.parseInt(semInfo);
            cap=Integer.parseInt(capacity);
            if(batch.equalsIgnoreCase("A")){
                batchNo=(semNo*10)+1;
            }
            else if(batch.equalsIgnoreCase("B")){
                batchNo=(semNo*10)+2;
            }
        return true;
        }
        
    }  
    
