    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here
        boolean isValidInput =validateInput();
        if(isValidInput==false)
        {
            JOptionPane.showMessageDialog(null,"Please Fill All details !!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            Semester s=new Semester(batchNo,semNo,batch,cap);
            int flag=SemesterDao.AddSemester(s);
            if(flag==1)
            {
                JOptionPane.showMessageDialog(null,"Semester Added ","Success",JOptionPane.INFORMATION_MESSAGE);
DefaultTableModel tMOdel = (DefaultTableModel) tblSemester.getModel();
tMOdel.setRowCount(0);
                showDataInTable();

            }
            else
            {
                JOptionPane.showMessageDialog(null,"Error","Cann't Add New Semester",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error"+ex,"Exception",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnAddMouseClicked

